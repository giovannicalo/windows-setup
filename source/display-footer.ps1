function displayFooter {
	Write-Host
	Write-Host "------------------------------------------------------------------------"
	Write-Host "|                          " -NoNewline
	Write-Host ">>> [ALL DONE] <<<" -ForegroundColor "Green" -NoNewline
	Write-Host "                          |"
	Write-Host "------------------------------------------------------------------------"
	Write-Host
	Write-Host "You should reboot."
	Write-Host
}
